/**
 * Structured logging with Pino
 */

import pino from 'pino';
import { config } from './config.js';

export const logger = pino({
    level: config.logLevel,
    transport: config.isDev
        ? {
            target: 'pino-pretty',
            options: {
                colorize: true,
                translateTime: 'HH:MM:ss',
                ignore: 'pid,hostname',
            },
        }
        : undefined,
});

export type Logger = typeof logger;
